#include "libft.h"

void				*ft_bzero(void *s, size_t n)
{
	char			*s_temp;
	size_t			i;

	s_temp = (char *)s;
	i = 0;
	while (i < n)
	{
		s_temp[i] = '\0';
		i++;
	}
	return (NULL);
}
